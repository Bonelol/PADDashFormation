// 安装阶段跳过等待，直接进入 active
self.addEventListener('install', function (event) {
    event.waitUntil(self.skipWaiting());
});

self.addEventListener('activate', function(event) {
	console.debug("Service Worker activate");
	const baseUrl = new URL(".", location);
	const baseUrlString = baseUrl.origin + baseUrl.pathname;
	
	event.waitUntil(
		Promise.all([
			self.clients.claim(),
			caches.keys().then(function(keyList) { //所有的现存的缓存大分类列表
				return Promise.all(
					keyList.map(async function(key) {
						if (cacheName !== key) { //如果不在当前的缓存列表里就删除
							console.debug("未检测到该缓存分类，删除", key);
							return caches.delete(key);
						} else {
							const cache = await caches.open(key);
							const requests = await cache.keys();
							console.debug("检测已有缓存分类", key);
							return Promise.all(
								requests.map(async function(request) {
									const url = new URL(request.url);
									const relativePath = (url.origin + url.pathname).replace(baseUrlString, "");
									const oldmd5 = url.searchParams.get("md5"); //老的md5值
									const md5 = cachesMap.get(relativePath); //新的md5值
									//console.debug("检测缓存条目", url, relativePath, oldmd5, md5);
									if (md5 && md5 !== oldmd5) {
										console.debug("删除版本不匹配的缓存", request);
										return cache.delete(request);
									}
								})
							);
						}
					})
				);
			})
		])
	);
});

self.addEventListener('fetch', async function(event) {
	const url = new URL(event.request.url);
	url.search = "";
	const baseUrl = new URL(".", location);
	const baseUrlString = baseUrl.origin + baseUrl.pathname;
	const relativePath = (url.origin + url.pathname).replace(baseUrlString, "");
	const md5 = cachesMap.get(relativePath); //事先记录的md5值
	//console.debug("请求网络", event.request.url, relativePath, md5);
	if (md5) {
		url.searchParams.set("md5", md5);
		const responseWithMd5 = await caches.match(url, {ignoreSearch: false});
		if (responseWithMd5) {
			console.debug("%c已有相同 md5 缓存，直接使用缓存", "color: green;", url);
			event.respondWith(responseWithMd5);
		} else {
			//console.debug("%c无相同 md5 缓存，重新在线获取", "color: blue;", url);
			let newResponse;
			try {
				newResponse = await fetch(event.request);
				console.debug("%c无相同 md5 缓存，重新在线获取结果", "color: blue;", url, newResponse);
				const cache = await caches.open(cacheName);
				await cache.put(url, newResponse.clone());
				//console.debug("%c储存新的Cache", "color: blue;", url, cache);
			} catch (error) {
				console.error("%c数据在线获取失败，尝试使用忽略 search 的离线数据", "color: red;", url, error);
				newResponse = await caches.match(url, {ignoreSearch: true});
			}
			event.respondWith(newResponse);
		}
	} else {
		console.debug("无 md5 值，重新在线获取", url);
		let newResponse;
		try {
			newResponse = await fetch(event.request);
			const cache = await caches.open(cacheName);
			await cache.put(url, newResponse.clone());
			event.respondWith(newResponse);
		} catch (error) {
			console.error("%c数据在线获取失败，尝试使用忽略 search 的离线数据", "color: red;", url, error);
			newResponse = await caches.match(url, {ignoreSearch: true});
		}
		event.respondWith(newResponse);
	}
});